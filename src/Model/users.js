class Users {
    constructor(id, Name, phoneNumber, email, username, password, category) {
        this.id = id; 
        this.Name = Name; 
        this.phoneNumber = phoneNumber; 
        this.email = email; 
        this.username = username; 
        this.password = password; 
        this.category = category;        
    }  
}

export default Users;
